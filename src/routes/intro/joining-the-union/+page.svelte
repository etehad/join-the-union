<h3>نحوه عضویت در اتحادیه</h3>

<p>
  عضویت در اتحادیه بر اساس پر کردن فرم یا روش های مشابه نیست، بلکه بر اساس همراهی با اتحادیه هست.
  برای عضویت باید حامی و همراه با مسیر اتحادیه باشید.
</p>

<p>تا زمان تشکیل شورای هماهنگی، اتحادیه فقط یک فعالیت داره:</p>

<p class="my-4 rounded bg-slate-200 px-2 py-1 dark:bg-slate-800">
  هر شب رأس ساعت ۹ عبارت «اتحادیه انقلاب ایران» را در گوگل جست و جو کنید و روی لینک این سایت کلیک
  کنید.
</p>

<p>
  تعداد اعضای اتحادیه که در صفحه‌ی اصلی این سایت نشون داده شده ترکیبی از تعداد جست‌وجو های این عبارت
  و تعداد کلیک های انجام شده روی لینک سایت در گوگل است.
</p>

<p>
  می‌تونید نمودار نسبی حمایت از اتحادیه رو در سایت خودِ گوگل هم ببینید. دقت کنید که نمودار گوگل نسبی
  هست و فقط نشون می‌ده حمایت از اتحادیه با چه سرعتی در حال کاهش یا افزایش هست. عدد‌ها تو سایت گوگل
  درصد هستند نه تعداد کلیک یا جست‌وجو.
